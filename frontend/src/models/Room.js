class Room {
    constructor(
        _id,
        name,
        acreage,
        price,
        facility,
        description,
        bed_type,
        property_id
    ) {
        this._id = id;
        this.name = name;
        this.acreage = acreage;
        this.price = price;
        this.facility = facility;
        this.description = description;
        this.bed_type = bed_type;
        this.property_id = property_id;
    }
}

export default Room;
